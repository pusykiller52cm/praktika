def is_parallelogram(A1, A2, A3, A4):
    # Вычисляем середины диагоналей
    mid1 = ((A1[0] + A3[0]) / 2, (A1[1] + A3[1]) / 2)
    mid2 = ((A2[0] + A4[0]) / 2, (A2[1] + A4[1]) / 2)
    
    # Если середины диагоналей совпадают, то это параллелограмм
    return mid1 == mid2

# Пример использования
A1 = (0, 0)
A2 = (4, 0)
A3 = (5, 3)
A4 = (1, 3)

result = is_parallelogram(A1, A2, A3, A4)
print(f"Являются ли точки вершинами параллелограмма: {result}")